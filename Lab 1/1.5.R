{
  n1 = as.integer(readline(prompt = "Enter a number: "))
  n2= as.integer(readline(prompt = "Enter a number: "))
  sum = n1 + n2
  diff = n1 - n2
  prod = n1 * n2
  div = n1 / n2
  print(paste("Sum = ", sum))
  print(paste("Difference = ", diff))
  print(paste("Product = ", prod))
  print(paste("Quotient = ", div))
}